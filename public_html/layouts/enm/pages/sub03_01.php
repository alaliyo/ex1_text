                <p class="yoo yoo1 scroll_on type_bottom">
                    부산세계로선교회는 (영문명 : Every Nation Mission, 약칭 ENM) 2000년 1월 1일 창립되었습니다. 전신은 한국네비게이토선교회입니다. 
                    1977년 3월 당시 해외선교부 대표로 재직 중인 변희관 목사부부에게 창22:17-18절 
                    “네 씨로 말미암아 천하 만민이 복을 얻으리라” 라는 약속의 말씀으로 부르시고 그 말씀에 순종하여 부산에 내려오며 시작되었습니다. <br><br>
                    하나님께서는 약속 말씀의 성취로 부산의 대학캠퍼스와 각 직장인들 속에서 수많은 예수님의 제자와 일꾼들이 길러지게 하셨고, 
                    그들이 한국 각 지역의 사역자로 파송되는 부흥을 허락하셨습니다.<br><br>
                    우리는 이에 안주하지 않았습니다. 하나님의 마음은 한국을 넘어 세계에 있었습니다. 
                    당시는 비자문제로 해외로 나가는 문이 사실상 닫혀 있었지만 하나님은 변목사 부부에게 
                    10년내로 100명의 선교사를 파송할 것이라는 비전을 보여주셨습니다.<br><br>
                    1986년 아시안게임, 1988년 올림픽 개최 성공 후 해외에 나가는 길들이 열리며 약속대로 100명의 장단기 선교사를 파송케 하셨습니다. 
                    우리는 기도했고, 믿음으로 순종했을 뿐이고 하나님께서 모든 것을 이루셨습니다.<br><br>
                </p>
            </div>
            <p class="yoo yoo2 scroll_on type_bottom">
            부산세계로선교회는 전문적인 선교기관으로 거듭나며 선교사들을 선발하고 훈련하고 파송하여 그들의 사역을 지원하는 선순환적인 일원화된 시스템을 갖추게 되었습니다. 
            또한 파송된 대부분이 자비량 선교사로서 선교활동을 감당하고 있다는 사실은 지금도 한국 선교사역에 중요한 한 획을 긋는 사건이자 좋은 본으로 남아 있습니다. 
            보다 효율적이고 국제적이며 타선교단체와의 적극적인 협력사역을 위해 2001년 1월 세계로선교회를 창립하게 되어 오늘에 이르게 되었습니다. <br><br>
            부산세계로선교회는 지난 45년간 오로지 한길만을 걸어왔습니다. 
            우리의 역사는 단순한 과거의 기록으로 끝나지 않고 다시금 새로운 출발을 시작합니다. 
            아브라함에게 주신 약속이 이삭과 야곱으로 이어지며 지금 우리에게까지 전달이 되어 성취되고 있습니다. <br><br>
            물이 바다를 덮음같이 하나님의 영광을 인정하는 것이 온 땅에 가득할 때까지, 각 나라와 족속과 백성과 방언에서 
            허다한 무리들이 모여서 하나님을 예배하는 그날이 오기까지 우리 부산세계로선교회는 하나님이 주신 사명, 
            곧 가서 모든 민족으로 제자를 삼고 충성된 일꾼들을 재생산하여 지상사명 성취하는 그 일에 전심전력하는 착하고 충성되고 지혜로운 하나님의 사람들로 살아갈 것입니다.<br>
            <strong class="bible-3-1">(합 2:14) 물이 바다를 덮음 같이 여호와의 영광을 인정하는 것이 세상에 가득함이니라</strong>
            </p>
